"""
graph/state.py
==============
Shared LangGraph State Schema for the Banking Multi-Agent Loan & Property System.

This TypedDict defines every field that flows through the StateGraph.
All agents READ from this state and WRITE partial updates back.
LangGraph merges updates automatically via its reducer logic.

Design principles:
  - Every field is Optional with a default of None so agents can safely
    check `if state["field"]` without KeyError on first invocation.
  - Nested structures use typed dicts (not plain dicts) for IDE support.
  - Enum-style string literals are used for status/decision fields to
    keep routing logic in edges.py readable and typo-proof.
  - All monetary values are in INR (Indian Rupees) as float.
  - All scores are normalised 0–100 as float unless noted otherwise.
"""

from __future__ import annotations

from typing import Any, Literal, Optional, TypedDict


# ---------------------------------------------------------------------------
# Nested sub-schemas (used as field types inside BankingState)
# ---------------------------------------------------------------------------


class ChatMessage(TypedDict):
    """
    A single turn in the conversation between the user and the system.

    Used by:  orchestrator_agent, faq_agent, recommendation_agent
    """

    role: Literal["user", "assistant", "system"]
    # "user"      → message typed by the human
    # "assistant" → response generated by an agent or LLM
    # "system"    → internal status message shown in the UI timeline

    content: str
    # Raw text of the message. Markdown is supported for assistant turns.

    agent: Optional[str]
    # Which agent produced this message, e.g. "faq_agent", "recommendation_agent".
    # None for user messages.

    timestamp: Optional[str]
    # ISO-8601 UTC timestamp string, e.g. "2025-06-01T10:32:00Z".
    # Set by the agent at write time.


class DocumentData(TypedDict):
    """
    Extracted fields from a single uploaded document.

    Populated by:  kyc_agent (identity docs), property_agent (property docs)
    Read by:       kyc_agent, loan_eligibility_agent, recommendation_agent
    """

    doc_type: Literal[
        "aadhaar", "pan", "passport", "voter_id",
        "salary_slip", "bank_statement", "itr", "form_16",
        "sale_deed", "property_tax_receipt", "encumbrance_cert", "registry"
    ]
    # Which document category this extraction belongs to.

    raw_text: Optional[str]
    # Full OCR text extracted from the document before LLM field parsing.

    extracted_fields: dict[str, Any]
    # Key-value pairs parsed from the document.
    # Examples:
    #   Aadhaar  → {"name": "Ramesh Kumar", "dob": "1988-04-12", "uid": "XXXX-XXXX-7890"}
    #   PAN      → {"pan_number": "ABCDE1234F", "name": "Ramesh Kumar"}
    #   Salary   → {"employer": "Infosys Ltd", "gross_salary": 95000, "month": "2025-05"}
    #   Sale deed→ {"owner_name": "Ramesh Kumar", "area_sqft": 1200, "registration_date": "2019-11-20"}

    verified: bool
    # True if the mock KYC API or land registry API confirmed the document is valid.

    verification_source: Optional[str]
    # Which mock API confirmed it, e.g. "mock_kyc_api", "mock_land_registry_api".


class CustomerProfile(TypedDict):
    """
    Full profile of an authenticated customer, loaded from customers.json
    by account_discovery_agent via mock_customer_lookup().

    Read by:  kyc_agent (skip check), risk_agent, credit_agent,
              loan_eligibility_agent, recommendation_agent
    """

    customer_id: str
    name: str
    mobile: str
    email: Optional[str]
    pan: str
    aadhaar_hash: str          # Hashed; never store raw Aadhaar
    date_of_birth: str         # ISO-8601 date string "YYYY-MM-DD"
    address: str

    account_number: Optional[str]
    account_type: Optional[Literal["savings", "current", "salary"]]
    account_balance: Optional[float]   # INR

    employment_type: Optional[Literal["salaried", "self_employed", "business", "retired"]]
    employer_name: Optional[str]
    employment_months: Optional[int]   # Months at current employer

    monthly_income: Optional[float]    # INR, gross
    existing_emi_total: Optional[float] # INR, sum of all active EMIs

    transaction_summary: Optional[dict[str, Any]]
    # Aggregated from mock_transaction_analysis_api():
    # {
    #   "avg_monthly_credit": 92000,
    #   "avg_monthly_debit":  61000,
    #   "cash_flow_stability": "stable",   # "stable" | "volatile" | "declining"
    #   "months_analysed": 12
    # }

    existing_loans: Optional[list[dict[str, Any]]]
    # Each entry: {"loan_type": "home", "outstanding": 1200000, "emi": 18000, "bank": "HDFC"}


class PropertyReport(TypedDict):
    """
    Consolidated output of property_agent after calling all four
    property-related mock APIs.

    Read by:  orchestrator (routing), risk_agent, loan_eligibility_agent,
              recommendation_agent
    """

    property_id: str
    address: str
    owner_name: str

    # --- Ownership & Legal ---
    ownership_verified: bool
    legal_disputes: bool           # True = active litigation found
    mortgage_status: Literal["clear", "mortgaged", "partially_released"]
    encumbrance_clear: bool        # True = no encumbrance found
    registry_valid: bool

    # --- Valuation ---
    market_value: float            # INR, from mock_property_valuation_api
    carpet_area_sqft: float
    property_type: Literal["apartment", "independent_house", "plot", "commercial"]
    property_age_years: int

    # --- Location ---
    latitude: float
    longitude: float
    flood_zone: bool
    transit_distance_km: float     # Distance to nearest metro/railway
    area_quality_tier: Literal["premium", "good", "average", "below_average"]

    # --- Risk Indicators ---
    crime_score: float             # 0–100; higher = more crime
    area_risk_rating: Literal["low", "medium", "high"]

    # --- Bank Relationship ---
    is_tied_with_bank: bool
    # True → fast-track loan flow; skip full risk+credit pipeline

    # --- Composite Score ---
    property_score: float          # 0–100; computed by property_service.py
    property_score_breakdown: dict[str, float]
    # e.g. {"ownership": 25.0, "legal": 20.0, "valuation": 30.0, "location": 25.0}


class RiskReport(TypedDict):
    """
    Output of risk_agent. Combines fraud detection and transaction analysis.

    Read by:  loan_eligibility_agent, recommendation_agent, escalation_agent
    """

    risk_score: float              # 0–100; higher = riskier
    risk_band: Literal["low", "medium", "high", "very_high"]

    fraud_risk_score: float        # 0–100, from mock_fraud_detection_api
    identity_mismatch: bool        # Name/DOB inconsistency across documents
    watchlist_hit: bool            # Customer flagged in any watchlist

    employment_stability: Literal["stable", "moderate", "unstable"]
    cash_flow_stability: Literal["stable", "volatile", "declining"]

    debt_burden_ratio: float       # existing_emi_total / monthly_income
    # e.g. 0.35 means 35% of income already committed to EMIs

    risk_narrative: str
    # LLM-generated plain-English summary, e.g.:
    # "The applicant shows stable employment at a reputed firm for 36 months.
    #  Transaction history is consistent with declared income. Fraud risk is low."

    flags: list[str]
    # Specific flags raised, e.g. ["high_cash_withdrawal", "salary_inconsistency"]


class CreditReport(TypedDict):
    """
    Output of credit_agent. Aggregates three bureau mock scores.

    Read by:  loan_eligibility_agent, recommendation_agent, escalation_agent
    """

    cibil_score: int               # 300–900
    experian_score: int            # 300–900
    equifax_score: int             # 300–900
    aggregated_score: int          # Median of the three bureau scores

    creditworthiness_rating: Literal["excellent", "good", "fair", "poor", "very_poor"]
    # excellent → 800+, good → 700–799, fair → 600–699,
    # poor → 500–599, very_poor → <500

    delinquency_count: int         # Number of missed payments in last 24 months
    credit_utilization: float      # 0.0–1.0; fraction of revolving credit used
    oldest_account_years: float    # Age of oldest credit account

    bureau_responses: dict[str, Any]
    # Raw responses from each mock bureau for audit trail:
    # {"cibil": {...}, "experian": {...}, "equifax": {...}}


class LoanDecision(TypedDict):
    """
    Final structured output of loan_eligibility_agent.

    Read by:  recommendation_agent, escalation_agent, API response layer
    """

    decision: Literal[
        "approved",
        "partial_approval",
        "conditional_approval",
        "inspection_required",
        "rejected"
    ]

    eligible_amount: float         # INR; maximum loan amount approved
    requested_amount: Optional[float]  # INR; what the user asked for

    interest_rate: float           # Annual %, e.g. 8.75
    loan_tenure_months: int        # e.g. 240 for 20-year home loan
    monthly_emi: float             # INR; calculated EMI at approved amount

    dti_ratio: float               # Debt-to-income ratio post loan, e.g. 0.42
    ltv_ratio: float               # Loan-to-value ratio, e.g. 0.75

    repayment_probability: float   # 0.0–1.0; model confidence of full repayment

    conditions: list[str]
    # Conditions for conditional_approval or inspection_required:
    # ["Submit NOC from existing lender", "Property inspection mandatory"]

    rejection_reasons: list[str]
    # Non-empty only when decision == "rejected":
    # ["Credit score below minimum threshold", "DTI exceeds 55%"]

    policy_rules_applied: dict[str, Any]
    # Snapshot of which rules from policy_rules.json were decisive:
    # {"min_credit_score": 650, "max_dti": 0.55, "max_ltv": 0.80}


class AgentStatus(TypedDict):
    """
    Execution status of a single agent node.

    Stored in state["agent_statuses"] as a dict keyed by agent name.
    Read by:  StatusPage.tsx via GET /api/status/{session_id}
    """

    status: Literal["pending", "running", "done", "failed", "skipped"]
    started_at: Optional[str]      # ISO-8601 timestamp
    completed_at: Optional[str]
    error_message: Optional[str]   # Set if status == "failed"
    duration_seconds: Optional[float]


class EscalationReport(TypedDict):
    """
    Output of escalation_agent. Structured brief for human review.

    Read by:  recommendation_agent, API response layer
    """

    escalation_id: str             # UUID for tracking
    reason: str                    # Short reason, e.g. "Borderline credit score"
    severity: Literal["low", "medium", "high"]

    summary: str                   # LLM-generated case summary for the reviewer
    flags_raised: list[str]
    suggested_checks: list[str]    # e.g. ["Verify employment with HR", "Site visit"]

    assigned_to: Optional[str]     # Mock staff name or None if unassigned
    created_at: str


