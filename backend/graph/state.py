class BankingState(TypedDict):
    """
    Master state dictionary shared across all LangGraph agent nodes.

    LangGraph passes this dict into every node function and merges
    the returned partial dict back into the global state.

    Organised into six logical sections:
      1. Session & User Classification
      2. Authentication & Customer Identity
      3. KYC & Document Handling
      4. Property Analysis
      5. Financial Assessment (Risk + Credit + Eligibility)
      6. Decision & Output
      7. Orchestration & Observability
    """

    # -----------------------------------------------------------------------
    # SECTION 1 — Session & User Classification
    # -----------------------------------------------------------------------

    session_id: str
    # Unique ID for this loan application session.
    # Generated by the API layer on first POST /api/chat.
    # Used as the LangGraph thread_id for checkpointing.
    # Set by: API layer (main.py)
    # Read by: all agents, checkpointer, status endpoint

    user_type: Optional[Literal["anonymous", "authenticated"]]
    # Determined by orchestrator_agent based on whether a valid
    # auth token was provided in the initial request.
    # "anonymous"     → routed to faq_agent only
    # "authenticated" → routed to auth_agent, then full loan pipeline
    # Set by: orchestrator_agent
    # Read by: edges.py (route_by_user_type)

    intent: Optional[Literal["faq", "loan_application", "status_check", "document_upload"]]
    # What the user wants to do, classified by orchestrator_agent
    # from the first user message.
    # Set by: orchestrator_agent
    # Read by: edges.py

    messages: list[ChatMessage]
    # Full conversation history including user messages and agent responses.
    # Accumulates across turns. The LLM agents (faq, recommendation,
    # escalation) receive this as their chat history context.
    # Set by: every agent that produces a user-visible message
    # Default: []

    # -----------------------------------------------------------------------
    # SECTION 2 — Authentication & Customer Identity
    # -----------------------------------------------------------------------

    is_authenticated: Optional[bool]
    # True after auth_agent successfully validates the session token
    # via mock_auth_api(). False if token is invalid or expired.
    # Set by: auth_agent
    # Read by: edges.py (route_after_auth)

    customer_id: Optional[str]
    # Unique customer identifier returned by mock_auth_api().
    # Used as the lookup key for all subsequent mock API calls.
    # Set by: auth_agent
    # Read by: account_discovery_agent, risk_agent, credit_agent,
    #           mock_customer_lookup, mock_transaction_analysis_api

    is_existing_customer: Optional[bool]
    # True if customer_id found in customers.json with kyc_status == "verified".
    # Existing customers skip full KYC and only provide updated documents
    # when specifically required.
    # Set by: account_discovery_agent
    # Read by: edges.py (route_after_account_discovery)

    customer_profile: Optional[CustomerProfile]
    # Full customer record loaded from customers.json.
    # None for new customers until KYC completes.
    # Set by: account_discovery_agent (existing) or kyc_agent (new)
    # Read by: risk_agent, credit_agent, loan_eligibility_agent,
    #           recommendation_agent

    # -----------------------------------------------------------------------
    # SECTION 3 — KYC & Document Handling
    # -----------------------------------------------------------------------

    kyc_status: Optional[Literal["not_started", "in_progress", "verified", "failed", "skipped"]]
    # Lifecycle status of the KYC process.
    # "skipped" is set for existing customers who already have verified KYC.
    # "failed"  triggers escalation_agent.
    # Set by: kyc_agent, account_discovery_agent (→ "skipped")
    # Read by: edges.py (route_after_kyc), loan_eligibility_agent

    uploaded_documents: Optional[list[DocumentData]]
    # All documents uploaded by the user in this session, with extracted fields.
    # Grows as the user uploads files via POST /api/documents/upload.
    # Set by: kyc_agent (identity docs), property_agent (property docs)
    # Default: []

    kyc_verification_result: Optional[dict[str, Any]]
    # Raw response from mock_kyc_api() for audit trail.
    # {"name_match": True, "dob_match": True, "address_verified": True}
    # Set by: kyc_agent

    missing_documents: Optional[list[str]]
    # Documents still required from the user.
    # e.g. ["itr", "encumbrance_cert"]
    # Set by: kyc_agent, property_agent
    # Read by: orchestrator_agent (to prompt user for uploads),
    #           recommendation_agent

    # -----------------------------------------------------------------------
    # SECTION 4 — Property Analysis
    # -----------------------------------------------------------------------

    property_id: Optional[str]
    # ID of the property being offered as collateral.
    # Provided by the user during the loan application form.
    # Set by: orchestrator_agent (extracted from user input)
    # Read by: property_agent

    property_report: Optional[PropertyReport]
    # Full consolidated output of property_agent.
    # Set by: property_agent
    # Read by: edges.py (route_by_property_status), risk_agent,
    #           loan_eligibility_agent, recommendation_agent

    property_status: Optional[Literal["tied", "untied", "verification_failed"]]
    # Simplified routing flag derived from property_report.is_tied_with_bank.
    # "tied"               → fast-track loan flow (skip risk + credit agents)
    # "untied"             → full risk + credit assessment required
    # "verification_failed"→ escalate
    # Set by: property_agent (derived from PropertyReport)
    # Read by: edges.py (route_by_property_status)

    requested_loan_amount: Optional[float]
    # INR amount the customer wants to borrow.
    # Extracted from user's message by orchestrator_agent.
    # Set by: orchestrator_agent
    # Read by: loan_eligibility_agent

    # -----------------------------------------------------------------------
    # SECTION 5 — Financial Assessment
    # -----------------------------------------------------------------------

    risk_report: Optional[RiskReport]
    # Output of risk_agent.
    # None if property is tied (fast-track skips risk assessment).
    # Set by: risk_agent
    # Read by: loan_eligibility_agent, recommendation_agent, escalation_agent

    credit_report: Optional[CreditReport]
    # Output of credit_agent.
    # None if property is tied (fast-track skips credit assessment).
    # Set by: credit_agent
    # Read by: loan_eligibility_agent, recommendation_agent, escalation_agent

    fast_track: Optional[bool]
    # True when property_status == "tied".
    # Signals loan_eligibility_agent to use relaxed underwriting rules
    # and skip risk/credit thresholds (bank already holds the asset).
    # Set by: edges.py or property_agent
    # Read by: loan_eligibility_agent, recommendation_agent

    loan_decision: Optional[LoanDecision]
    # Final structured loan output from loan_eligibility_agent.
    # Set by: loan_eligibility_agent
    # Read by: recommendation_agent, escalation_agent, API response

    # -----------------------------------------------------------------------
    # SECTION 6 — Decision & Output
    # -----------------------------------------------------------------------

    recommendation_text: Optional[str]
    # LLM-generated customer-facing recommendation letter.
    # Summarises property score, risk band, credit rating, eligibility,
    # EMI breakdown, and next steps in plain language.
    # Set by: recommendation_agent
    # Read by: API response → ChatWidget.tsx in frontend

    requires_human_review: Optional[bool]
    # True when escalation_agent is triggered.
    # Causes the API to include an escalation notice in the response.
    # Set by: escalation_agent
    # Read by: API response layer, recommendation_agent

    escalation_report: Optional[EscalationReport]
    # Structured brief for the human reviewer.
    # Set by: escalation_agent
    # Read by: API response layer (exposed to bank staff dashboard)

    # -----------------------------------------------------------------------
    # SECTION 7 — Orchestration & Observability
    # -----------------------------------------------------------------------

    agent_statuses: dict[str, AgentStatus]
    # Execution status of every agent, keyed by agent name.
    # e.g. {"auth_agent": {"status": "done", ...},
    #        "kyc_agent":  {"status": "skipped", ...}}
    # Updated by each agent at start and completion.
    # Read by: GET /api/status/{session_id} → StatusPage.tsx (AgentTimeline)
    # Default: {}

    current_agent: Optional[str]
    # Name of the agent currently executing.
    # Set by each agent at the start of its run.
    # Used by the SSE stream to push live "agent X is running" events.

    errors: list[dict[str, Any]]
    # Accumulated non-fatal errors from any agent.
    # Each entry: {"agent": "property_agent", "code": "API_TIMEOUT",
    #              "message": "mock_land_registry_api timed out after 3s",
    #              "timestamp": "2025-06-01T10:35:12Z"}
    # Fatal errors set agent_statuses[agent]["status"] = "failed"
    # and may trigger escalation_agent.
    # Default: []

    retry_counts: dict[str, int]
    # How many times each agent has retried a failed API call.
    # e.g. {"property_agent": 2}
    # Used by base_agent.py to enforce max_retries = 3.
    # Default: {}

    graph_version: str
    # Version string of the compiled graph, e.g. "1.0.0".
    # Stamped onto every session for debugging and A/B testing.
    # Set by: graph_builder.py at compile time.

    created_at: str
    # ISO-8601 UTC timestamp when this session was first initialised.
    # Set by: API layer (main.py) on session creation.

    updated_at: str
    # ISO-8601 UTC timestamp of the last state mutation.
    # Updated by every agent after writing to state.
    # Used to detect stale/abandoned sessions.


# ---------------------------------------------------------------------------
# Default state factory
# ---------------------------------------------------------------------------

def make_initial_state(session_id: str, graph_version: str = "1.0.0") -> BankingState:
    """
    Returns a BankingState with all optional fields set to None/[]/{}.

    Call this in main.py when a new session starts:

        state = make_initial_state(session_id=str(uuid4()))
        app.invoke(state, config={"configurable": {"thread_id": state["session_id"]}})
    """
    from datetime import datetime, timezone

    now = datetime.now(timezone.utc).isoformat()

    return BankingState(
        # Section 1 — Session
        session_id=session_id,
        user_type=None,
        intent=None,
        messages=[],

        # Section 2 — Auth & Identity
        is_authenticated=None,
        customer_id=None,
        is_existing_customer=None,
        customer_profile=None,

        # Section 3 — KYC & Documents
        kyc_status=None,
        uploaded_documents=[],
        kyc_verification_result=None,
        missing_documents=[],

        # Section 4 — Property
        property_id=None,
        property_report=None,
        property_status=None,
        requested_loan_amount=None,

        # Section 5 — Financial Assessment
        risk_report=None,
        credit_report=None,
        fast_track=None,
        loan_decision=None,

        # Section 6 — Output
        recommendation_text=None,
        requires_human_review=None,
        escalation_report=None,

        # Section 7 — Orchestration
        agent_statuses={},
        current_agent=None,
        errors=[],
        retry_counts={},
        graph_version=graph_version,
        created_at=now,
        updated_at=now,
    )


